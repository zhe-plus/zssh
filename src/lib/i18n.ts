function dict(lang: string | null | undefined): Dict {
  if (lang === "en-US") return EN_US;
  if (lang === "zh-TW") return ZH_TW;
  return ZH_CN;
}

export function t(lang: string | null | undefined, key: keyof typeof ZH_CN): string {
  const d = dict(lang);
  return d[key] ?? ZH_CN[key] ?? String(key);
}

export function tf(lang: string | null | undefined, key: keyof typeof ZH_CN, vars: Record<string, string | number>): string {
  const s = t(lang, key);
  return s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}
